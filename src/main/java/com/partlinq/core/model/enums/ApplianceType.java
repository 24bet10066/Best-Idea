package com.partlinq.core.model.enums;

/**
 * Enumeration of appliance types supported by PartLinQ.
 * Represents different categories of electrical appliances for which parts are available.
 */
public enum ApplianceType {
	/** Air Conditioning units */
	AC,
	/** Refrigerators */
	REFRIGERATOR,
	/** Washing machines */
	WASHING_MACHINE,
	/** Microwave ovens */
	MICROWAVE,
	/** Water purifiers */
	WATER_PURIFIER,
	/** Electric water heaters */
	GEYSER,
	/** Dishwashers */
	DISHWASHER,
	/** Kitchen chimneys/range hoods */
	CHIMNEY,
	/** Air coolers */
	AIR_COOLER,
	/** Television sets */
	TELEVISION,
	/** Other appliance types */
	OTHER
}
